    public void UpdateData(){
        PreparedStatement ps;
        ResultSet rs;
        DefaultTableModel table = (DefaultTableModel)jTable1.getModel();
        String id = table.getValueAt(jTable1.getSelectedRow(), 0).toString();
        int a = Integer.parseInt(id);
        String name = jTextField_Name.getText();
        String camera = jTextField_Camera.getText();
        String processor = jTextField_Processor.getText();
        String ram = jTextField_Ram.getText();
        String rom = jTextField_Rom.getText();
        String battery = jTextField_Battery.getText();
        String fingerprint = jTextField_FingerPrint.getText();
        String display = jTextField_Display.getText();
        String os = jTextField_os.getText();
        String price = jTextField_price.getText();
        int vom = Integer.parseInt(jTextField_vom.getText());
        String Query = "UPDATE `phones` SET `name`=?,`camera`=?,`processor`=?,`ram`=?,`rom`=?,`battery`=?,`fingerprint`=?,`display`=?,`os`=?,`price`=?,`value`=? WHERE `id`=?";
        try {
            ps = phoneCNX.getConnection().prepareStatement(Query);
            ps.setString(1, name);
            ps.setString(2, camera);
            ps.setString(3, processor);
            ps.setString(4, ram);
            ps.setString(5, rom);
            ps.setString(6, battery);
            ps.setString(7, fingerprint);
            ps.setString(8, display);
            ps.setString(9, os);
            ps.setString(10, price);
            ps.setInt(11, vom);
            ps.setInt(12, a);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Successfully Updated! by "+uname);
            showTable();
         
        } catch (SQLException ex) {
            java.util.logging.Logger.getLogger(Update.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        }
    }
    
