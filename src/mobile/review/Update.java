        table.addColumn("ID");
        table.addColumn("Name");
        table.addColumn("Camera");
        table.addColumn("Processor");
        table.addColumn("RAM");
        table.addColumn("ROM");
        table.addColumn("Battery");
        table.addColumn("Finger Print");
        table.addColumn("Display");
        table.addColumn("OS");
        table.addColumn("Price");
        table.addColumn("VOM(%)");
        
        PreparedStatement st;
        ResultSet rs;
        
        String query = "SELECT id,name,camera,processor,ram,rom,battery,fingerprint,display,os,price,value FROM phones";
        try {           
            st = phoneCNX.getConnection().prepareStatement(query);
            rs = st.executeQuery();
            while(rs.next()){
                table.addRow(new Object[]{
                    rs.getString(1),
                rs.getString(2),
                rs.getString(3),
                rs.getString(4),
                rs.getString(5),
                rs.getString(6),
                rs.getString(7),
                rs.getString(8),
                rs.getString(9),
                rs.getString(10),
                rs.getString(11),
                rs.getString(12),
                });
            }
            jTable1.setModel(table);
            //jTable1.setModel(DbUtils.resultSetToTableModel(rs));
            
            //Column Auto Size
            TableColumnModel col = jTable1.getColumnModel();
            TableModel model = jTable1.getModel();
            int total = col.getColumnCount();
            for(int i=0;i<total;i++){
                int taile = 0;
                int total2 = model.getRowCount();
                for(int j=0;j<total2;j++){
                    if(model.getValueAt(j, i)!=null){
                        int taile2 = model.getValueAt(j, i).toString().length()*5;
                        if(taile2>taile){
                            taile = taile2;
                        }
                    } col.getColumn(i).setPreferredWidth(taile);
                }
            }
            
        } catch (SQLException ex) {
            java.util.logging.Logger.getLogger(Update.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        }
        
